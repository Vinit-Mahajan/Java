class NegativePositive{
	public static void main(String[] args) {
		int no = -1;
		if(no < 0) {
			System.out.println(no + " is negative number");
		}
		else if(no > 0){
			System.out.println(no + " is positive number");
		} else {
			System.out.println(no + " is neither positive nor Negative number");				
		}
	}
}
