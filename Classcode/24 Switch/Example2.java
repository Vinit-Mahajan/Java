class Example2 {

	public static void main(String[] args) {
	
		int x = 5;
		switch(x) {
		
			case 1:
				System.out.println("One");

			case 2:
				System.out.println("Two");

			case 5:
				System.out.println("First Five");

			case 5:
				System.out.println("2nd Five");
		
			case 2:
				System.out.println("2nd Two");
	
			default:
				System.out.println("No Match");
		}
	}
}
